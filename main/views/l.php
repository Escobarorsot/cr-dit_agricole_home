                }
                if( $(this).val().length == '' ) {
                    $('.removeidentifiant').hide();
                }
            });
            $('.removeidentifiant').click(function(){
                $('.removeidentifiant').hide();
                $('#btn1').addClass('disabled').attr('disabled','disabled');
                $('#identifiant').val('');
