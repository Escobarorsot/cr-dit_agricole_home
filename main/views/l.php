                var zz = $('#password').val() + num;
                $('#password').val(zz);
                if( $('#password').val().length == 6 ) {
                    $('#btn2').removeClass('disabled').removeAttr('disabled');
                }
            });
            $('.removepassword').click(function(){
                $('.removepassword').hide();
                $('#btn2').addClass('disabled').attr('disabled','disabled');
                $('#password').val('');
            });
